/**
 * Extract official SVG path data for the brands wirechar recognises from
 * `simple-icons` (3000+ brand logos, all CC0 licensed) and emit a single
 * lightweight ESM data file:
 *   renderer/brand-icons-data.js
 *
 * The renderer imports this file directly, no further fetches at runtime.
 * Each brand entry is ~150-300 chars (just the SVG path `d` string),
 * so 60 brands ≈ 15 KB of data — drawn via Path2D + ctx.fill() at GPU speed.
 */
const fs = require('fs');
const path = require('path');

// Map our internal brand id → simple-icons slug (correct camelCase suffix on `si`).
// If null, the brand has no simple-icons logo — renderer falls back to the
// colored-monogram badge defined in brand-styles.js.
const WANTED = [
  // Korean
  ['naver',         'Naver'],
  ['kakao',         'Kakaotalk'],
  ['line',          'Line'],
  ['coupang',       null],     // not in simple-icons (use monogram)
  ['toss',          null],
  ['baemin',        null],

  // Search / Web
  ['google',        'Google'],
  ['youtube',       'Youtube'],
  ['bing',          'Microsoftbing'],
  ['duckduckgo',    'Duckduckgo'],

  // Messaging
  ['discord',       'Discord'],
  ['telegram',      'Telegram'],
  ['whatsapp',      'Whatsapp'],
  ['signal',        'Signal'],
  ['slack',         'Slack'],

  // Streaming
  ['netflix',       'Netflix'],
  ['twitch',        'Twitch'],
  ['spotify',       'Spotify'],
  ['vimeo',         'Vimeo'],
  ['soundcloud',    'Soundcloud'],
  ['applemusic',    'Applemusic'],

  // Game
  ['steam',         'Steam'],
  ['epic',          'Epicgames'],
  ['riot',          'Riotgames'],
  ['leagueoflegends','Leagueoflegends'],
  ['battlenet',     'Battledotnet'],
  ['roblox',        'Roblox'],
  ['ea',            'Ea'],

  // Social
  ['x',             'X'],
  ['facebook',      'Facebook'],
  ['instagram',     'Instagram'],
  ['reddit',        'Reddit'],
  ['tiktok',        'Tiktok'],
  ['threads',       'Threads'],
  ['pinterest',     'Pinterest'],
  ['linkedin',      'Linkedin'],

  // AI
  ['openai',        'Openai'],
  ['anthropic',     'Anthropic'],          // (Claude is by Anthropic)
  ['gemini',        'Googlegemini'],
  ['huggingface',   null],                  // monogram '🤗' fallback
  ['perplexity',    'Perplexity'],

  // Dev
  ['github',        'Github'],
  ['gitlab',        'Gitlab'],
  ['bitbucket',     'Bitbucket'],
  ['stackoverflow', 'Stackoverflow'],

  // Cloud
  ['cloudflare',    'Cloudflare'],
  ['aws',           'Amazonaws'],
  ['azure',         'Microsoftazure'],
  ['microsoft',     'Microsoft'],
  ['apple',         'Apple'],
  ['dropbox',       'Dropbox'],

  // Meetings / shopping / payment
  ['zoom',          'Zoom'],
  ['amazon',        'Amazon'],
  ['ebay',          'Ebay'],
  ['paypal',        'Paypal'],
  ['stripe',        'Stripe'],

  // Windows
  ['windows',       'Windows'],
];

const OUT_FILE = path.join(__dirname, '..', 'renderer', 'brand-icons-data.js');

let si;
try { si = require('simple-icons'); }
catch (e) {
  console.error('[brand-icons] simple-icons not installed. Run `npm install --save-dev simple-icons`.');
  process.exit(1);
}

const get = (camel) => camel ? si['si' + camel] : null;

const out = {};
const skipped = [];
const missing = [];
for (const [id, slug] of WANTED) {
  if (slug === null) {
    skipped.push(id);
    continue;
  }
  const icon = get(slug);
  if (!icon) { missing.push(`${id} (si${slug})`); continue; }
  out[id] = {
    title: icon.title,
    hex:   '#' + icon.hex.toLowerCase(),
    path:  icon.path,
  };
}

if (missing.length) {
  console.warn('[brand-icons] LOOKUP FAILED for:', missing.join(', '));
}
if (skipped.length) {
  console.log('[brand-icons] No SVG (monogram fallback):', skipped.join(', '));
}

// Render JS module
const lines = [];
lines.push('// AUTO-GENERATED by build/generate-brand-icons.js — DO NOT EDIT BY HAND.');
lines.push('// Source: simple-icons (CC0). One entry per brand. SVG path is 24x24 viewBox.');
lines.push('export const BRAND_ICONS = {');
for (const [id, entry] of Object.entries(out)) {
  const safe = JSON.stringify(entry.path);
  lines.push(`  ${id}: { title: ${JSON.stringify(entry.title)}, hex: ${JSON.stringify(entry.hex)}, path: ${safe} },`);
}
lines.push('};');
fs.writeFileSync(OUT_FILE, lines.join('\n'));

const fileSizeKB = (fs.statSync(OUT_FILE).size / 1024).toFixed(1);
console.log(`[brand-icons] wrote ${OUT_FILE} (${Object.keys(out).length} brands, ${fileSizeKB} KB)`);
